package Gun16;

public class _02_forLoop {
    public static void main(String[] args) {
        //While döngüsü
        int i=0;
        while (i < 5)
        {
            System.out.println("w_i = " + i);
            i++;
        }

        // for döngüsü
        for(int j=0;j<5;j++)
        {
            System.out.println("f_j = " + j);
        }

    }
}
